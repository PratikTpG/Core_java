package Constructer;

public class ConstructerDocs {
    /* -----------_Constructor------------
            * # use to create object in java
            * # Special type of method which has the same name as class name os known as a constructer.
            *
            * # if there is no any user defined constructor in class then java provides default constructer.
            * # no return type.
       TYPES
            * Default Constructor:
                -> use to create object and set default values to it
                    #syntax :

                            class_name(){
                             //Set default values to object.
                            }
            * Parameterized Constructor :
              -> use to create object and set dynamic values to it.
                     #Syntax:
                           class_name(args....){
                           // set dynamic values to object.
                           }
     */
}
